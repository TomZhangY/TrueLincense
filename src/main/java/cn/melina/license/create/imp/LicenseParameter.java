package cn.melina.license.create.imp;

public class LicenseParameter {
	private String privatealias;
	private String keyPwd;
	private String storePwd;
	private String subject;
	private String licPath;
	private String priPath;

	public String getPrivatealias() {
		return privatealias;
	}

	public void setPrivatealias(String privatealias) {
		this.privatealias = privatealias;
	}

	public String getKeyPwd() {
		return keyPwd;
	}

	public void setKeyPwd(String keyPwd) {
		this.keyPwd = keyPwd;
	}

	public String getStorePwd() {
		return storePwd;
	}

	public void setStorePwd(String storePwd) {
		this.storePwd = storePwd;
	}

	public String getSubject() {
		return subject;
	}

	public void setSubject(String subject) {
		this.subject = subject;
	}

	public String getLicPath() {
		return licPath;
	}

	public void setLicPath(String licPath) {
		this.licPath = licPath;
	}

	public String getPriPath() {
		return priPath;
	}

	public void setPriPath(String priPath) {
		this.priPath = priPath;
	}
}
